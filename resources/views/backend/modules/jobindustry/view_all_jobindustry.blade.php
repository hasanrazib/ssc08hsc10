<script type="text/javascript">

function editData(id){

    $.ajax({
        type:"GET",
        dataType: "json",
        url:"/get-job-industry/edit/" + id,

        success:function(data){

            var html = '';

            $.each(data,function(key,v){
                html += '<input type="hidden"  class="form-control form-control-solid" value="'+v.id+'" name="id"/>';
                html += '<input type="text"  class="form-control form-control-solid" value="'+v.jobindustry_name+'" name="jobindustry_name"/>';

            });

            $('#jobindustry_name').html(html);
        }
    })
}


</script>


